package com.github.phuctranba.core.item;


public class ItemReview {

    private String ReviewId;
    private String ReviewUserName;
    private String ReviewUserId;
    private String ReviewUserAvata;
    private String ReviewTime;
    private String ReviewRate;
    private String ReviewMessage;
    private String ReviewParentId;

    public String getReviewId() {
        return ReviewId;
    }

    public void setReviewId(String reviewId) {
        ReviewId = reviewId;
    }

    public String getReviewUserName() {
        return ReviewUserName;
    }

    public void setReviewUserName(String reviewUserName) {
        ReviewUserName = reviewUserName;
    }

    public String getReviewUserId() {
        return ReviewUserId;
    }

    public void setReviewUserId(String reviewUserId) {
        ReviewUserId = reviewUserId;
    }

    public String getReviewUserAvata() {
        return ReviewUserAvata;
    }

    public void setReviewUserAvata(String reviewUserAvata) {
        ReviewUserAvata = reviewUserAvata;
    }

    public String getReviewTime() {
        return ReviewTime;
    }

    public void setReviewTime(String reviewTime) {
        ReviewTime = reviewTime;
    }

    public String getReviewRate() {
        return ReviewRate;
    }

    public void setReviewRate(String reviewRate) {
        ReviewRate = reviewRate;
    }

    public String getReviewMessage() {
        return ReviewMessage;
    }

    public void setReviewMessage(String reviewMessage) {
        ReviewMessage = reviewMessage;
    }

    public String getReviewParentId() {
        return ReviewParentId;
    }

    public void setReviewParentId(String reviewParentId) {
        ReviewParentId = reviewParentId;
    }
}
